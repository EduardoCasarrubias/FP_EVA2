public class Eva2_15_acumuladores {

    
    public static void main(String[] args) {
        //acomulador
        // ++, --, +=, -=, /=
        int cuenta = 0;
        //asignador --->
        System.out.println(cuenta);
        cuenta = cuenta + 100; //acomulador 
        System.out.println(cuenta);
        cuenta = cuenta + 1000;
        System.out.println(cuenta);
        cuenta = cuenta - 800;
        System.out.println(cuenta);
        cuenta += 2000; //cuenta = cuenta + 2000;
    }
    
}
