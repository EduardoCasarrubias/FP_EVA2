public class Eva2_18_while {

    
    public static void main(String[] args) {
        int i =1;
        System.out.println("iclo while----->");
        while(i <= 10 ){//se repite si la condiciones es verdad 
            System.out.println("eduardo casarrubias");
            i++;
        }
        //similar un for con un while 
        System.out.println("ciclo do while----->");
        i=1;
        do{
            System.out.println("eduardo casarrubias");
            i++;
        }while(i<=10);
        
    }
    
}
